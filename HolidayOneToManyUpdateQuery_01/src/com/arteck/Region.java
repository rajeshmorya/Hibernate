package com.arteck;

public class Region {

	private Integer regionid;
	private String regionname;
	private String regionaddress;
	private Integer foreven_companyid;
	/**
	 * @return the regionid
	 */
	public Integer getRegionid() {
		return regionid;
	}
	/**
	 * @param regionid the regionid to set
	 */
	public void setRegionid(Integer regionid) {
		this.regionid = regionid;
	}
	/**
	 * @return the regionname
	 */
	public String getRegionname() {
		return regionname;
	}
	/**
	 * @param regionname the regionname to set
	 */
	public void setRegionname(String regionname) {
		this.regionname = regionname;
	}
	/**
	 * @return the regionaddress
	 */
	public String getRegionaddress() {
		return regionaddress;
	}
	/**
	 * @param regionaddress the regionaddress to set
	 */
	public void setRegionaddress(String regionaddress) {
		this.regionaddress = regionaddress;
	}
	/**
	 * @return the foreven_companyid
	 */
	public Integer getForeven_companyid() {
		return foreven_companyid;
	}
	/**
	 * @param foreven_companyid the foreven_companyid to set
	 */
	public void setForeven_companyid(Integer foreven_companyid) {
		this.foreven_companyid = foreven_companyid;
	}
}
